package com.hz.example.pojo;

public class Emp {
	private String name;
	private Integer age;
	
	public Emp() {
		this.name = "tom";
		this.age = 18;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer getAge() {
		return age;
	}
	public void setAge(Integer age) {
		this.age = age;
	}
	
	
}
